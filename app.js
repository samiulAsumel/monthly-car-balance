function ensureMonth(y, m) {
  const key = mk(y, m);
  if (DB[key]) return;
  const pk = m === 1 ? mk(y - 1, 12) : mk(y, m - 1);
  const prevRows = DB[pk] || [];
  let openBals;
  if (prevRows.length) {
    // Use the actual closing balances from last day of previous month
    const lastDay = prevRows[prevRows.length - 1];
    openBals = calcLocBals(lastDay.bal, lastDay.del, lastDay.imp);
  } else {
    openBals = LOCS.map(() => 0);
  }
  DB[key] = [];
  let locBals = [...openBals];
  for (let d = 1; d <= dIn(y, m); d++) {
    const ds = `${y}-${String(m).padStart(2, "0")}-${String(d).padStart(2, "0")}`;
    if (d === 1) {
      DB[key].push({
        date: ds,
        del: LOCS.map(() => 0),
        imp: LOCS.map(() => 0),
        bal: [...openBals],
        al: "",
        av: "",
      });
    } else {
      const prev = DB[key].slice(-1)[0];
      const newBals = calcLocBals(prev.bal, prev.del, prev.imp);
      DB[key].push({
        date: ds,
        del: LOCS.map(() => 0),
        imp: LOCS.map(() => 0),
        bal: newBals,
        al: "",
        av: "",
      });
    }
  }
}

function recalcFrom(key, fi) {
  const rows = DB[key];
  // Starting point: for fi=0, keep opening bals as-is (they're hardcoded)
  // For fi>0, recalc from fi using prev row
  for (let i = fi; i < rows.length; i++) {
    if (i === 0) continue; // day 1 opening bal is kept
    const prev = rows[i - 1];
    rows[i].bal = calcLocBals(prev.bal, prev.del, prev.imp);
  }
}

function getClosing(row) {
  return calcClosing(row.bal, row.del, row.imp);
}

function summ(key) {
  const rows = DB[key] || [];
  if (!rows.length)
    return {
      bal: 0,
      del: 0,
      imp: 0,
      end: LOCS.map(() => 0),
      dl: LOCS.map(() => 0),
      il: LOCS.map(() => 0),
    };
  let td = 0,
    ti = 0;
  const dl = LOCS.map(() => 0),
    il = LOCS.map(() => 0);
  rows.forEach((r) => {
    r.del.forEach((v, i) => {
      td += v;
      dl[i] += v;
    });
    r.imp.forEach((v, i) => {
      ti += v;
      il[i] += v;
    });
  });
  const last = rows.slice(-1)[0];
  const lastClosing = getClosing(last);
  return {
    bal: lastClosing,
    del: td,
    imp: ti,
    end: last.bal.slice(),
    dl,
    il,
  };
}

