function onDel(key, ri, li, val) {
  requireLogin(() => {
    const validatedValue = validateAndShowError(
      { value: val },
      "Delivery",
      0,
      9999,
    );
    pushUndo();
    DB[key][ri].del[li] = validatedValue;
    recalcFrom(key, ri);
    setDirty(true);
    renderAll();
  });
}
