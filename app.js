function pushUndo() {
  undoStack.push({ key: cur, data: JSON.parse(JSON.stringify(DB[cur])) });
  if (undoStack.length > 50) undoStack.shift();
}
function undoLast() {
  if (!undoStack.length) {
    alert("Nothing to undo.");
    return;
  }
  requireLogin(() => {
    const { key, data } = undoStack.pop();
    DB[key] = data;
    recalcFrom(key, 1);
    setDirty(true);
    renderAll();
  });
}

